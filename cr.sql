-- Membuat tabel CONNECTION
CREATE TABLE CONNECTION (
    user_number TEXT NOT NULL,
    bot_number TEXT NOT NULL,
    value TEXT,
    PRIMARY KEY (user_number, bot_number)
);

-- Membuat tabel CONVERSATION
CREATE TABLE CONVERSATION (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    user_number TEXT NOT NULL,
    bot_number TEXT NOT NULL,
    timestamp INTEGER NOT NULL,
    content TEXT NOT NULL
);

-- Membuat tabel PARTICIPANT
CREATE TABLE PARTICIPANT (
    GROUPID TEXT PRIMARY KEY,
    GROUPNAME TEXT NOT NULL
);

-- Membuat tabel CONTACTS
CREATE TABLE CONTACTS (
    CONTACT_ID TEXT PRIMARY KEY,
    CONTACT_NAME TEXT NOT NULL,
    PRIVATE_CHAT BOOLEAN DEFAULT FALSE
);

-- Membuat tabel TOKENS
CREATE TABLE TOKENS (
    USER_NUMBER TEXT NOT NULL,
    TIMESTAMP INTEGER NOT NULL,
    PROMPT_TOKENS INTEGER NOT NULL,
    COMPLETION_TOKENS INTEGER NOT NULL,
    TOTAL_TOKENS INTEGER NOT NULL
);

-- Membuat tabel INFO_CS
CREATE TABLE INFO_CS (
    USER_NUMBER TEXT NOT NULL,
    TIMESTAMP INTEGER NOT NULL
);
